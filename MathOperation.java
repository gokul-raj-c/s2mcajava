class MathOperation
{
public static void main(String args[])
{
int a=10;
int b=5;
int sum=a+b;
int diff=a-b;
int pro=a*b;
float quo=a/b;
int rem=a%b;
System.out.println("sum of "+a+" and "+b+" is "+sum);
System.out.println("difference of "+a+" and "+b+" is "+diff);
System.out.println("product of "+a+" and "+b+" is "+pro);
System.out.println("quotient of "+a+" and "+b+" is "+quo);
System.out.println("modulus of "+a+" and "+b+" is "+rem);
}
}
